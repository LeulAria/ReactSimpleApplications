function darkTheme() {
  document.documentElement.style.setProperty('--primary', '#333');
  document.documentElement.style.setProperty('--secondary', '#f4f4f4');
  document.documentElement.style.setProperty('--dark-primary', '#2a2a2a');
  document.documentElement.style.setProperty('--light-primary', '#d0d0d0');
}; 

function lightTheme() {
  document.documentElement.style.setProperty('--primary', '#f4f4f4');
  document.documentElement.style.setProperty('--secondary', '#333');
  document.documentElement.style.setProperty('--dark-primary', '#e7e7e7');
  document.documentElement.style.setProperty('--light-primary', '#555');
}; 
